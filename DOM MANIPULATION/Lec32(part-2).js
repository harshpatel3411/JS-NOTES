// Using innerHTML
let element1 = document.getElementById("example1");
console.log("innerHTML:", element1.innerHTML); // Outputs: This is <strong>bold</strong> text.


// Using innerText
let element2 = document.getElementById("example2");
console.log("innerText:", element2.innerText); // Outputs: This is italic text.


// Using textContent
let element3 = document.getElementById("example3");
console.log("textContent:", element3.textContent); // Outputs: This is underlined text.

